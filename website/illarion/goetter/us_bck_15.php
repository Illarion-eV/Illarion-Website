<h1>Findari - Goddess of Air</h1>

<?php cap(F); ?>
<p>indari is the goddess of the element Air. It is not in Findari's nature to be compulsive but as with any wind she gave change to the world - for better or for worse, that is always a matter of perspective. Kind in person, she is known as a friendly goddess without bad intentions. Tthose who do not understand her ways, however, won't regard the devastation by storms she brings as a chance to start anew, but as sheer destruction. If Findari ever takes on a mortal form, she is dressed mostly in light blue and appears as an ageless blond-haired woman, dancing even where others might be lost in despair. Music and fine arts are her preference, as long as they are not enforced by rules but rather are used to express personal thoughts and feelings.</p>

<h2>Followers</h2>
<p>Findari's believers were always few, since she never cared much about temples and being worshipped. It is said that she still chooses some rare followers herself to become visionaries or wandering prophets, or perhaps even great bards and artists. As with all the Elder Gods, she is worshipped at the Temple of the Five, east of Galmair. There is also the Chapel of the Five, east of the Kantabi Desert, where the name of the Five is held in honour. Sailors of Runewick are known to pray to her, but more in fear of storms than true admiration.</p>

<h2>Findos - Month of Fine Arts</h2>

<p>Findos is one of the main harvesting months and at this time of year the fields around Runewick and Galmair are a hive of activity. As the peasants are busy working during Findos, few festivities are known at this time of the year. Findari is fine with that, as freedom from recurrent obligations matters more to her than being worshipped.</p>

