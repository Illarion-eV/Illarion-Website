<p>ie ersten Formen menschlicher Schrift waren zu Beginn vereinfachte Versionen des elfischen Alphabets und nur einige
wenige zeigten tatsächlich Interesse daran. Es waren zumeist Könige und andere Fürsten, Priester und Händler, welche aus
verschiedensten Gründen die Vorzüge der Schrift erkannten. Für die einen war es notwendig, sich auf einen
allgemeingültigen Codex zu stützen, und diesen auch schriftlich niederzulegen. Andere nutzen es dazu, zum Teil geheime
Botschaften untereinander auszutauschen. Erst sehr viel später entwickelte ein König der Menschen, dessen Name der
Vergessenheit anheim gefallen ist, eine sehr einfache und damit für die breitere Masse der Bevölkerung nutzbare Schrift.
Es stellte sich heraus, dass aufgrund der elementaren Form dieser Schrift man in die Lage versetzt war, nicht nur die
menschliche Sprache zu Papier zu bringen, sondern auch die Sprache der Zwerge, Elfen und Orks. Durch diesen Umstand begünstigt,
entwickelte sich die Sprache der Menschen zu einer beliebten Handelssprache, wodurch auch Wörter und Aussprüche der Elfen,
Zwerge und Orks in den Wortschatz Aufnahme fanden. So entstand im Laufe der Zeit die so genannte Einheitssprache, welche auch
heute noch, mit Einflüssen von lokalen Dialekten durchsetzt, die Muttersprache der Menschen ist.</p>

