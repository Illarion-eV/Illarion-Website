<p>ie Ama–shoon (Steppenvolk) sind im engeren Sinne kein eigenes Volk, sondern man kann sie eher als eine unabhängige Kriegerkaste
des Steppenvolks bezeichnen. Die Mitglieder dieses Stammes bestehen ausschließlich aus Frauen. Männliche Kinder werden im Alter von
vier Jahren zu den Ansiedlungen der Serinjah gebracht, wo sie fortan von ihren Vätern aufgezogen werden.<br />
Die Ama–shoon leben in lockeren Waldgebieten und Ebenen in der Nähe der Serinjah. Sie haben ihr Leben dem Kampf und dem Schutz der
Natur und des Landes gewidmet. Sie sind Anhänger der Göttin Findari (Göttin der Luft und der vier Winde). Schon in jungen Jahren werden
sie im Umgang mit allen Waffengattungen ausgebildet, doch ist ihre bevorzugte Waffe der Bogen und Speer. Es wird auch berichtet,
dass sie, wenn notwendig, sehr effektiv mit bloßen Händen kämpfen können. Sie bevorzugen verstärkte Leder oder Holzrüstungen, wenn
auch Panzerungen aus Metall nicht unbekannt sind, und sie ziehen es vor, ohne Sattel zu reiten. Im Kampf wird von ihnen auch
aggressive Magie eingesetzt, wobei ihnen Praktiken der regenerierenden und heilenden Kunst nicht unbekannt sind. Die Anführerin
eines Ama–shoon Stammes ist mitunter eine Findari–Priesterin.</p>

<h3>Kulturvölker:</h3>
<p>Diese Völker sprechen die Einheitssprache. Allen ist gemeinsam, dass die Ehe als ein Bündnis fürs Leben geehrt wird und nur unter bestimmten Voraussetzungen wieder gelöst werden kann.</p>

<?php cap(D); ?>
<p>ie Salkamaerianer (Volk des Lichts) leben in Städten, Dörfern und Burgsiedlungen. Die Wurzeln dieses Volkes und ihre Lebensweise
reichen zurück in die alte Stadt Salkamar, welche auch der Ursprung der Einheitssprache und des Schriftsystems ist.<br />
Das Leben der Salkamaerianer wird von Einstellungen bestimmt wie Respekt, Ehre und Loyalität. Sie führen ein nobles Leben. Allen
Kindern wird Lesen und Schreiben beigebracht. Sklaverei ist per Gesetz verboten und den Mitgliedern von mittellosen Familien wird
eine militärische Ausbildung umsonst angeboten. Die Könige und der Adel der Salkamaerianer werden als Vertreter der Interessen des
Volkes angesehen und ihre Führung ist häufig weise und klug. Die Städte werden von männlichen oder weiblichen Feudalherren regiert;
ein Beweis für die relative ausgeglichene Position zwischen den Geschlechtern. Die Gesellschaft der Salkamaerianer ist recht offen
gegenüber Fremden und sie haben in nur geringem Umfang Vorurteile gegenüber anderen Kulturen und Völkern.<br />
Ihre Poeten, Barden und Philosophen sind in der ganzen Welt berühmt, wie auch die Salkamaerianischen Paladine, die auf Wanderschaft
gehen und für Rechtschaffenheit und Menschlichkeit kämpfen. Zhambra, Oldra, Sirani, Elara and Malachín sind die am häufigsten
angebeteten Götter in den Ansiedlungen der Salkamaerianer.</p>

<?php cap(D); ?>
<p>ie Albarianer (Volk der Ordnung) leben in kleinen Städten, Dörfern und Burgsiedlungen. Ihre Gesellschaftsform ist die einer
Leibeigenschaft. Das Land wird von Adelsgeschlechtern regiert und alle Personen, die sich darauf befinden, sind Besitztum des
Fürsten. Der überwiegende Teil der Bevölkerung sind Leibeigene ohne festgeschriebene Rechte und damit der Willkür des Adels
ausgeliefert. Ein Adeliger kann seine Leibeigenen jederzeit zum Dienst an der Waffe oder zu anderen Arbeiten anfordern. Die
Strafe für Ungehorsam dem Adeligen gegenüber ist meist grausam.<br />
Die Frauen der Albarianer sind ihren Männern oder Vätern untergeordnet; ja, selbst dem Fürsten gegenüber, denn die Adeligen
haben immer noch das Recht der ersten Nacht in diesen Landen. Es verwundert nur wenig, dass weiblichen Gottheiten nur wenig Ehre
zuteil wird.<br />
Der Ursprung der Kultur der Albarianer liegt in der Stadt Albar. Sie ist etwas älter und ursprünglicher als die Kultur der
Salkamaerianer und hat sich in die entgegengesetzte Richtung entwickelt. Da die Not der Leibeigenen dazu führt, dass auf dem
Gebiet der Albarianer Rebellion und Anarchie keine Seltenheit ist, sind die Regionen unter der Führung der Albarianer am Schwinden.
Die zentralen Gottheiten variieren, meist in Abhängigkeit der regierenden Fürstenfamilie.</p>

<?php cap(D); ?>
<p>ie Gynkeesh (Freies Volk) leben in unabhängigen Städten und Gemeinden. Sklaverei ist zwar per Gesetz verboten, doch die
einflussreichen und vermögenden Handelsfamilien scheinen dies ohne große Bedenken zu vergessen. Der größte Einflussfaktor
in der Gesellschaft der Gynkeesh ist Gold und Silber. Die Stadträte werden von den reichen Familien und Handelsorganisationen
dominiert. Ihre Schutzarmeen und Wächter sind meist bezahlte Söldner, die sich der besten Bezahlung hingeben.<br />
Die überwiegend arme Bevölkerung lebt in den Elendsvierteln der Städte; eine unabhängige Handwerksschicht existiert meist nicht.
Die Städte sind ein Hort für Bettler und andere Gestrandete des Lebens. Auch gibt es Diebesgilden, die mit viel Geschick einen
einflussreichen Machtfaktor bilden. Die meisten Ansiedlungen der Gynkeesh bestehen aus einem Sammelsurium an unterschiedlichsten
Völkern und Kulturen. Boshafte Zungen bezeichnen diese Städte auch als „Misthaufen“ oder „Sündenpfuhl“.</p>

