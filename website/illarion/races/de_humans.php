<h3>Barbarenvölker:</h3>

<?php cap(D); ?>
<p>ie Norodaj (Seevolk) bewohnen raue und kalte Klimazonen. Sie sind für gewöhnlich in Fell, Leder oder auch gefärbte raue Stoffe
gekleidet. Die Männer tragen meist lange Bärte und bevorzugen im Kampf lange Schwerter, Speere, Äxte und auch Wurfäxte.
Ihr Schriftbild ist in Anlehnung an die Kultur der Zwerge ein Runenalphabet. Sie nutzen zwar auch die Einheitssprache aber ihre eigene
Muttersprache ist sehr stark mit zwergischen Wörtern durchsetzt. In den steinigen, nördlichen Gebieten bewohnen sie den schmalen
Landstreifen zwischen den Bergen und der See. In ihrer Nachbarschaft sind fast immer Zwerge zu finden, mit denen sie enge
Handelsbeziehungen pflegen. Auch haben sie zu den ortsansässigen Echsenmenschen freundschaftliche Beziehungen. Ihre
Hauptnahrungsquelle bildet das Meer. Sie betreiben Fischfang, besitzen aber auch Schafherden, welche sie wild an den angrenzenden
Berghängen grasen lassen. Zur Fortbewegung und zum Fischfang benutzen sie lange und sehr bewegliche Ruderbote, die nur mit einem
Segel ausgerüstet sind. Gegen die Unbilden des Wetters auf hoher See schützt eine dicke Stoffplane, welche quer über dem Boot
gespannt ist. Ihre Boote sind recht leicht an den hölzernen Wolfsköpfen zu erkennen, welche auch als Rammbock dienen. Ihr Auftauchen
an entfernten Küstenregionen ist gefürchtet, denn sie sind bekannt dafür, die Dörfer von Orks und auch fremde Menschenansiedlungen
zu überfallen.<br />
Die Norodaj beten zu verschiedenen Göttern, doch aufgrund ihrer Lebensweise werden hauptsächlich Tanora (Göttin des Meeres),
Irmorom (Gott des Handels und Handwerks) sowie Malachín (Gott des Krieges und der Jagd) verehrt. In ihrer Gesellschaft wird Magie
als etwas Unnatürliches angesehen; etwas, mit dem ein Sterblicher sich nicht befassen sollte. Deswegen schauen sie mit Argwohn
und Hass, zum Teil auch mit großer Furcht, auf jede magisch aktive Person. Diese Angst erstreckt sich auf das gesamte Volk der
Elfen, denen sie hin und wieder auf dem Meer begegnen. Es erscheint ihnen, als ob die ruderlosen Schiffe der Elfen auf
geheimnisvolle Weise im Nebel auftauchen und wieder verschwinden, und dies mit einer für sie nicht erklärbaren Geschwindigkeit.
Sie gelten als Unheil bringende Geisterschiffe, ein Fluch des Meeres, und Unglück überkommt all die, welche ihrer angesichtig werden.<br />
Norodaj leben in einem großen Familienklan. Der Klanführer hat eine fast uneingeschränkte Macht über die Geschicke des Stammes.
Die einzige Möglichkeit der Einflussnahme besteht im Ausrufen des großen Wettstreits, welcher abgehalten wird, um einen neuen Klanführer
zu ermitteln. Um in diesem Wettstreit zu bestehen, sind sowohl Intellekt wie auch Kraft gefordert. Der Gewinner wird fortan
„Champion“ genannt und fordert den Klanführer zu einem Duell heraus. In einigen wenigen Fällen kommt es hierbei zu einem
Kampf auf Leben und Tod. Doch meistens ist es ein rein symbolischer Kampf, in welchem dem alten Klanführer die Möglichkeit gegeben
wird, sich ehrenvoll zurückzuziehen. Der Klanführer hat das Recht sich bis zu drei Frauen zu nehmen, alle anderen Klanmitglieder
dürfen nur eine Frau heiraten. Eine Heirat bindet das Paar auf Lebenszeit und kann unter normalen Umständen nicht wieder gelöst
werden. Ehemänner, welche betrogen wurden, fordern den Nebenbuhler zu einem Duell heraus, welcher auf jeden Fall mit dem Tod eines
der beiden enden wird. Männer, die ihre Frau oder Kinder schlecht behandeln, werden aus dem Klan ausgestoßen.<br />
Verstorbene werden in einem Ritual verbrannt, wobei der Leichnam auf einem hoch aufgeschichteten Scheiterhaufen dem Feuer übergeben
wird. Die Asche der Verstorbenen wird in das Meer gestreut und kleine Teile des Schädels werden aufbewahrt und zum Schutz vor
Geistern bei einem neuen Schiff an dem hölzernen Wolfskopf befestigt. Jeder Klan hat auch einen Geisterführer in ihren Reihen,
welcher eine Art Schamane oder Priester ist.</p>

