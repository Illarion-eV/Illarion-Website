<p>ie Serinjah (Steppenvolk) leben in den großen Ebenen. Sie züchten Pferde und Rinder und leben in verstreuten Ansiedlungen in Hütten
aus Holz. Auch ihre Gesellschaft besitzt eine Klanstruktur und sie sind bekannt als außergewöhnlich gute Reiter, Tierbändiger, Jäger
und Krieger. Serinjah bevorzugen im Kampf Speere und Bögen, welche sie meisterlich im Kampf zu Pferd einsetzen. Doch sind sie auch im
Umgang mit Langschwert und Langdolch, Wurfstern und Wurfmesser bewandert. Aufgrund des hohen Gewichts und der Einschränkung in der
Bewegungsfähigkeit meiden sie im Kampf Rüstungen aus Stahl sondern tragen eher einen Schutz aus Leder oder auch Holz. Die Serinjah
haben eine besondere Form des waffenlosen Kampfes entwickelt, bei dem Ellenbogen, Knie, Hände und Füße eingesetzt werden, um den Gegner
auszuschalten – einige dieser Kampfformen erinnern sehr stark an Bewegungsabläufe der elfischen Kriegstänzer.<br />
Die Einheitssprache ist dem Steppenvolk bekannt, doch sprechen sie von alters her einen urtümlichen menschlichen Dialekt und einige
sind sogar in der Elfensprache bewandert. Sie treiben viel Handel mit den Elfen, aber auch mit Orks und anderen Menschenvölker. Ihre
zentrale Gottheit ist Malachín, Gott des Krieges und der Jagd, aber sie beten auch zu allen anderen Jungen Göttern. Die Führung eines
Serinjah Klans hat ein Rat aus drei Personen inne. Dieser besteht aus einer jungen Frau (häufig ist dies eine Frau aus dem Volk der
Ama–Shoon), einem jungen Mann und einer alte Frau. Das Steppenvolk vertraut auf die Kraft der Natur und einige von ihnen besitzen
Kenntnisse in druidischer Magie. Doch auch alle anderen Formen der magischen Schulen sind ihnen nicht unbekannt und es gibt einige
unter ihnen, die das Leben eines Zauberers, Schamanen oder einer Hexe führen.<br />
Die Serinjah kennen die Institution der Heirat nicht. Ihre Verbindungen bestehen aus einem öffentlich ausgesprochenen
Versprechen, welches jederzeit wieder gelöst werden kann. Die Verstorbenen werden in der Erde begraben und zur Erinnerung
wird ein Stein auf den Gräbern aufgestellt. Nachdem das frische Grab vollständig von Gras überwuchert wurde, wird von den Angehörigen
und Freunden eine Zeremonie abgehalten, in dessen Bedeutung der Sieg des Lebens über den Tod gefeiert wird. Die Natur hat sich
durchgesetzt und beschließt damit den Kreislauf des Lebens.</p>

