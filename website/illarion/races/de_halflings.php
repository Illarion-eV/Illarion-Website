<p>ür die Unwissenden sehen die Halblinge wie die Kinder der Menschen aus und tatsächlich haben sie einige Ähnlichkeiten
mit der Statur der Menschen. Doch der augenfälligste Unterschied, neben ihrer geringen Körpergröße, sind ihre großen und
behaarten Füße, welche sie mit großem Stolz zur Schau tragen, da sie es vermeiden jedwede Art von Fußbedeckung zu tragen.
Sie neigen auch dazu, älter zu werden als die Menschen.</p>

<?php cap("H"); ?>
<p>alblinge bevorzugen es in hügeligen und dünn bewaldeten Gebieten zu leben, in denen sie ihre Behausungen in die Erde
graben. Über mehrere Generationen hinweg können diese Wohnhöhlen beachtliche Ausmaße annehmen, welche angefüllt
sind mit allerlei Möbeln und anderen Gegenständen, denn Halblinge bringen es nicht leicht übers Herz, Sachen wegzuwerfen,
deren Nützlichkeit sich eines Tages noch herausstellen mag. Es gibt auch einige wenige, die in Häusern ähnlich derer der
Menschen wohnen, doch die meisten Halblinge würden dies nicht als gemütlich ansehen. In der Regel leben sie in der Gemeinschaft
eines großen Familienklans, welcher von einem Familienoberhaupt geführt wird. Dies ist aber eher ein Ehrentitel und hat keine
wirkliche Funktion der Macht oder des Einflusses. Es wird aber erwartet, dass das Familienoberhaupt Feiern und große Festmahle
ausrichtet und insbesondere dafür sorgt, dass die Vorratskammern immer gut gefüllt sind, da es nicht unüblich ist, dass Halblinge
uneingeladen zum Essen erscheinen und man es dabei als sehr unhöflich betrachtet, den Tisch mit einem leeren Magen zu verlassen.
Böswillige Zungen behaupten auch, dass es nur drei Dinge gibt, welche die Halblinge als wichtig oder beachtenswert schätzen.
Dies wären Frühstück, Mittagessen und Abendbrot! Aber tatsächlich zeigen die Halblinge sehr viel Interesse an jedweder
Art von Nahrung oder Getränken und es wird ihnen nachgesagt, dass sie gerne Süßigkeiten naschen. Es mag auch durchaus
vorkommen, dass ein Halbling mitten in einem hitzigen Gefecht, sich in aller Ausführlichkeit über die Vorzüge einer bestimmten
Zubereitungsweise von Fleisch auslässt. Es verwundert daher nicht, dass sich viel Tavernenbesitzer damit brüsten, einen
Halbling als Koch angestellt zu haben. Auch bevorzugen gerade jüngere Halblinge Adron vor allen anderen Göttern.</p>

<?php cap("V"); ?>
<p>iele Halblinge ringen die Mittel zu ihrem Lebensunterhalt der Erde ab. Natürlich gibt es auch einige, die durch ihre
Meisterschaft in einem Handwerk bekannt wurden, aber im Allgemeinen sind sie begabte Bauern, Jäger und Kräuterkundige.
Der Anblick eines kleinen Schreins zu Ehren Oldras, gebaut um die Saat und Ernte zu begünstigen, ist kein seltener Anblick
in Gegenden, in denen Halblinge beheimatet sind. Auch ist das Erntedankfest, welches von ihnen Herbst begangen wird,
der wichtigste Feiertag im ganzen Jahr. Durch diese innige Nähe zur Natur haben die Halblinge ein fast angeborenes Talent
entwickelt, sich leise zu bewegen; auch sind sie recht behände und flink. Natürlich würden es viele Halblinge nicht offen
zugeben, aber bei einigen fanden diese Talente Anwendung in der Profession als Dieb und Taschendieb. Aus diesem Grund ist
man gut beraten ein Auge auf seine Habseligkeiten zu haben, wenn ein Halbling in der Nähe ist, der ein Medaillon mit dem
Emblem von Ronagan um den Hals trägt.</p>

<?php cap("H"); ?>
<p>alblinge haben in der Regel eine eher unbekümmerte Einstellung zum Leben. Im Gegensatz zu den Elfen, Zwergen und Menschen
sorgen sie sich selten um die Zukunft und dessen Folgen. Sie sind sehr angenehme und heitere Zeitgenossen, die es bevorzugen
ein Schwätzchen über Familienangehörige oder Freunde zu führen als über politischen Themen zu sprechen. Sie haben auch weder
Sinn für Hierarchien noch für höfliche Zurückhaltung, aber schätzen in hohem Maße den Wert der Familie und von Freunden.
Darüber hinaus sorgen sie sich liebevoll um ihre Nachkommen. Dieser Ansatz spiegelt sich wieder in den Strukturen, welche
ihre Gesellschaft dominiert. Es mangelt ihnen an einer allgemeingültigen politischen Struktur und einem Gefühl der Einheit.
Sie leben eher in sehr losen Gemeinschaften, welche aber von ihrem eigenen Volk dominiert sind, obwohl auch gemischte
Siedlungen mit dem Volk der Menschen bekannt sind.</p>

<?php cap("D"); ?>
<p>iese Ablehnung einer zentralen Führung hat in der Vergangenheit zu Fremdherrschaften geführt, jeweils einmal durch die
Menschen und durch die Orks. Obwohl die Zeit unter der Oberhoheit der Menschen weit weniger hart war als zur Zeit der
Orks, führte es in beiden Fällen unter den Halblingen zu großer Not. Und nur unter diesen beiden Umständen ist es
bekannt, dass die Halblinge ihren Widerwillen bezüglich einer zentralen Führungsgewalt aufgaben. Ihre kindliche Erscheinung
mag zu der Annahme führen, dass sie wehrlos sind, wie auch schon das Sprichwort sagt, dass es einfacher ist einen Berg zu
bewegen, als einen Halbling in Rage zu versetzten (mit der Ausnahme, dass man sich an seinem Essen vergreift, sollte man
hinzufügen). Aber sobald eine bestimmte Entschlusskraft in den halsstarrigen Kopf eines Halbling vorgedrungen ist, sollte
man sich wünschen, nicht in seinem Weg stehen zu bleiben. Sowohl die Orks wie auch die Menschen haben dies durch leidvolle
Erfahrung kennengelernt, als der Aufstand der Halblinge ihre Gesellschaften bis ins Mark erschütterten.</p>

<p>Magie, mit Sicherheit, ist nicht ihr primäres Talent. Mag es auch sein, dass einige wenige Halblinge sich an der Kunst der
Magie versuchen, so ist man doch gut beraten, wenn man dabei nicht allzu nahe bei ihnen steht, da der eine oder andere Spruch
nicht unbedingt in der Art ausfällt, wie er beabsichtigt war.</p>

