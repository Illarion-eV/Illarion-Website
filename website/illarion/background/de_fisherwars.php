<h2>-2121 bis -2119 Der Fischerkrieg</h2>

<p>Eine neue Norodaj-Menschensiedlung wurde um -2115 gegründet. Fischer dieses sehr
kleinen Dorfes finden in der Bucht einer kleinen Insel ein sehr reiches
Fischvorkommen.</p>

<p>Drei Wochen später kehrt nur eines von den fünf Fischerbooten zurück, die dorthin
gefahren waren. Die beiden Insassen berichten von gepanzerten grünen Echsenwesen, die
brüllend aus dem Wasser in die Boote gesprungen waren und sämtliche Insassen
töteten.</p>

<p>Ein Bote des Dorfes läuft noch am selben Tag los, um die Nachricht zu verbreiten.
Als er eine Woche später in sein Dorf zurückkehrt, ist selbiges völlig zerstört.</p>

<p>Drei Häuptlinge der Norodaj bilden eine Streitmacht, um die Insel zu erkunden und die
von dorther stammende Gefahr zu eliminieren. Es kommt zu mehreren Gefechten, die meisten
auf den Booten der Menschen. Die Zeit der Gefechte dauert mehre Monate lang an, bis
eines Morgens ein Echsenpriester, völlig ohne bewaffneten Geleitschutz, ein Dorf der
Menschen betritt und fordert, einen ihrer Clansoberhäupter zu sprechen.</p>

<p>In den schriftlichen Überlieferungen der Echsenmenschen heißt es, dass
Menschenfischer in einer heiligen Bucht gefischt, und dabei mit einem ihrer Netze eine
heilige Statuette beschädigt hatten. Dieser offensichtliche Akt der Aggression sowie die
Blasphemie sollten nicht ungesühnt bleiben : Die Tempelwächter töteten alle Fischer in
deren Booten. Die Siedlung wurde als Warnung zerstört, sämtliche Dorfbewohner
gefangengenommen.</p>

<p>In einem Abkommen zwischen Menschen und Echsen werden die Insel und umgebende
Gewässer zu heiligem Boden erklärt.</p>

