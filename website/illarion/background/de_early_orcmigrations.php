<h2>-2678 bis -2612 Die ersten Ork-Kriege</h2>

<p>Der Kriegsmeister des Dragonclaw-Clans, Tuchak Bloodfist, übernimmt durch Intrigen
und mehrere Morde die Führung des Clans und ernennt sich zum Kriegsherren aller Orks. Er
lässt die Ältesten der beiden anderen großen Clans (Wolfmoon-Clan und Firefist-Clan)
ermorden und setzt ihm ergebene Kriegsfürsten an deren Spitzen. In den darauffolgenden
Aufständen wird Tuchak Bloodfist getötet, und die drei großen Clans zersplittern in fünf
Clans (Bloodfist-Clan, Nighthawk-Clan, Howling-Wolf-Clan, Waraxe-Clan und
Bloody-Skull-Clan), sowie mehrere Kleine.</p>

<p>Der Bloodfist-Clan, welcher aus ehemaligen Anhängern Tuchaks besteht, wird von den
anderen vier Clans vertrieben und beginnt zu wandern. Nun geführt von Krugg Bloodfist
(einem jüngeren Bruder Tuchaks), überfällt und plündert der Clan zwei Steppensiedlungen
der Plainfolk-Menschen. In den darauffolgenden Jahren dringt der Bloodfist-Clan bis in
zivilisierte Gebiete vor, Späher der Orks erreichen die Stadt Albar.</p>

<p>Erstaunt vom Bericht über eine Befestigte Siedlung solcher Größe und mit der
Albarischen Armee im Nacken, zieht Krugg Bloodfist sich zurück und schickt Boten zu den
vier anderen großen Clans. Diese verweigern jegliche Unterstützung, und der
Bloodfist-Clan wird nahezu aufgerieben.</p>

<p>Um seinen Clan zu retten, opfert Krugg Bloodfist dreißig Gefangene und sich selbst in
einem Blutritual. Kurz darauf dezimiert eine große Horde Monster, von einem Dämon
angeführt, die Albarische Armee. Der Rest des Bloodfist-Clans zieht sich währenddessen
sicher an die Grenzen der orkischen Gebiete zurück.</p>

<p>Die Kunde von Dämonenmagie, Monstern und blutrünstigen Orks verbreitet sich schnell
in den Städten der Menschen. Samalkaerianische Paladine heuern Söldner an, um die einst
verfeindete Stadt Albar zu unterstützen, und das entstandene Heer findet schnell die
Unterstützung der Serinjah-Stämme. Der Bloodfist-Clan wird innerhalb eines halben Jahres
aufgerieben und verstreut. Die Menschliche Armee dringt weit in orkisches Gebiet vor und
errichtet eine Reihe befestigte Stellungen entlang des Draikon-Flusses. Über Jahrzehnte
hinweg werden von dort aus Angriffe auf die orkischen Clans geführt.</p>

