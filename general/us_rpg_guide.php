<h2>Know your character!</h2>

<p>There is no script, so there are no fixed roles. Therefore choose a role that you always
wanted to play. The only important thing is, that it fits to the setting of Illarion. An
engineer of the arms industry would not fit in, while a medieval blacksmith would.<br />
Once you have choosen a person, you should think about his personal background. The motivation
of your character should be derived from your background: What are your goals? What do you like
or hate?<br />
If you are talking to other people, you should be able to answer questions without a lot of
thinking. What are the names of your parents? Where were you born? How old are you? What does
your home look like? What is your religion? How do you talk? Do you speak with short and precise
words or tend to give endlessly long speeches? Are you using a noble tongue or are you talking
like a peasant? Do you have any speech defects or are you speaking a dialect?<br />
While working on your game character, keep in mind that the world does not only consist of super
heroes. Not every player should be the son of a king or an orphan searching for his lost
parents.</p>

