<p><a name="19"></a></p>

<h2>Evil guys</h2>

<p>The characters that are played with a bad style the most are "evil" characters. To
play a bad guy is really hard! Many dull persons just want to kill other people like in other
games. They have no reason why they kill and are just killing randomly.<br />
If you are playing a murderer for instance, you won't kill anyone without choice that comes
along. The risk of getting caught or killed yourself is too high. He will only kill where he has
a motive and will be relatively sure that no one will take revenge. Also he should have a nature
that enables him to kill without regret. Even a lunatic would not kill randomly. He also has a
story, how he got mad and perhaps there are some situations that provoke an outburst of rage.
All this must be logically from the persons background story.<br />
People tend to think of murderers if they think of "evil", even if a murder is perhaps
the most seldom crime. What about tricksters? Thieves? Rowdys? Schemers? Spys? Road robbers?
Blackmailers? Kidnappers? Badmouthing about the merchant concurrents? Conflicts between
neighbours? Peasants that put vermin in the barns of other peasants? Isn't it more pleasing
to infiltrate a guild, sell their secrets and set them against another guild, to get rid of two
guilds that are concurring with your (third) guild?<br />
Whatever type of "evil" you are playing, play him consistent. Don't be a fierce
robber on one day and, as soon as it gets attractive to take part on the life of a simple
citizen, play a friendly villager that takes part on the banquette of the king (where of course
many people know him as a robber, but don't act against him, because it is so nice sitting
together talking).</p>

