<h2>What is roleplaying?</h2>

<p>A roleplaying game is like a stage play, only that everyone can take part, and the plot is
open. What happens next depends mainly on your own behaviour. It is all about improvisation. Our
task as "creators" of Illarion is, to provide a set (the game world) and to enable all
players to enter this world (by setting up a server and providing the software). Your task as
player is to play a person in this stage play and to act - from the person's point of view -
logical. A computer software like this can only enable you to do things, that the programmers
thought of before, but you will surely have more ideas. So you must overcome this lack of
flexibility in the software by creativity, but more on this topic later.</p>

