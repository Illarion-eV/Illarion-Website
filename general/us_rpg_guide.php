<h2>Races</h2>

<p>Behave according to your race. There will always be persons that don't represent their
race, but they will be more seldom the more they differ from the "standard behaviour".
So don't try to play a dwarf that is seven feet high and that earns his money as a bard.
Also don't play an elf that earns his money by digging for worms in the mud to sell them to
fishermen. You can find descriptions of the races on the homepage.</p>

