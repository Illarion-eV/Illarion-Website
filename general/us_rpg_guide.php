<h2>OOC knowledge / Charakter seperation</h2>

<p>Who is taking an active role in the game will use the boards on our website for sure. You
will find a lot of information there. You should only use this information in game, if you
character was able to gather the information. If a player is describing on the boards, how his
character manages to sneak into a guild meeting and listen to it, this would be some information
that your character hasn't got for sure. If someone is writing a price list for his little
shop on the boards and adds a note to it, that the list is pinned to the flag pole on the market
place, your character can use this information.<br />
Similar things apply, when you are playing different characters (not at the same time). Things
that one of them finds out, aren't available for the other. Therefore it is generally
difficult to play two very active characters. Also no information, goods or money should be
traded between the two. It is absolutely a bad style to play a druid, only to mix free potions
for the magician from the same player.</p>

