<h2>-2,121 until -2,119: The Fisher War</h2>

<p>A new settlement of the Norodaj humans was founded around -2,115. Fishermen from this
very small town find a highly rich source of fish in the bay of a small island.</p>

<p>Three weeks later, only one of the five fisher boats returns from those who had
trailed there. The two seafarers reported of armored lizardpeople, who had lept roaring
from the water onto the boats and had slain all mates.</p>

<p>A messenger of the town ran off on the same day to spread the word. When he returns
to his town a week later, the town is levelled.</p>

<p>Three chieftains of the Norodaj assembled a force to explore the island and eliminate
the danger that eminated from it. This leads to several skirmishes, mostly taking place
upon the boats of the humans. The battles endure for several months, until one morning,
a priest of the lizardfolk approaches a town of the humans without any armed company and
demands to speak with one of their clan leaders.</p>

<p>In the scribed traditions of the lizardfolk it is stated that the human fishers had
been scavenging for fish in a holy bay, and that one of their nets had damaged a holy
statuette. This obvious act of aggression and blasphemy would not go without atonement:
The temple guards slew all fishers in their boats. The settlement was destroyed as a
warning, and the entire populace was captured.</p>

<p>In a treaty between the humans and lizardfolk, the island and surrounding waters were
declared as holy grounds.</p>

