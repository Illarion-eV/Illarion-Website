<h2>-2,100 until -2,030: Forthelm the Conqueror</h2>

<p>A heroic epic appears in human scriptures (the predecessor of the first common trade
language). It remains unclear till today, if this is simply a tale or a historic report.
Around -2,100, a young warrior named "Forthelm" supposedly united several towns,
defeated an evil dragon, and so founded a "great kingdom".</p>

<p>Around -2,030, a king named "Forthelm" founds the first standardized trade scripture,
which consists of letters. It is unlikely that these two "Forthelms" are the same
person.</p>

<p>Because of the diverse trade relationships, this scripture is used by elvenkind and
lizardfolk, later on even by dwarvenkind, but its usage dwindled after the fall of this
kingdom. The king died around -1,982 due to venerable age. Because the throne's
successor remained unclear (he had over thirty sons), several small and independant
lordships formed, which oftenly led wars amongst eachother.</p>

